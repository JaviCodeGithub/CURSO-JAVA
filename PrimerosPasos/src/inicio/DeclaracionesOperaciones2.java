package inicio;

public class DeclaracionesOperaciones2 {

	public static void main(String[] args) {
		
		final double apulgadas=2.54; //"final" define constante, no modificable
		
		double cm=6;
		
		double resultado=cm/apulgadas;
		
		System.out.println("En " + (int)cm + "cm hay " + resultado + " pulgadas");
		
		int operador1,operador2, resultados;
		
		operador1=9;
		operador2=7;
		resultados=operador1+operador2;
		
		System.out.println(resultados);
	}

}
